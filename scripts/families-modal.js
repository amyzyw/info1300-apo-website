// Marvels
$("#fam-ava").click(function() {
    if ($(window).width()>800) {
        $("#m-fam-ava").removeClass("hidden");
        $(".container").removeClass("hidden");
        $("#cancel-fam-ava").click(function() {
            $("#m-fam-ava").addClass("hidden");
            $(".container").addClass("hidden");
        });
    } else {
        if (!$("#m-fam-ava").hasClass("hidden")) {
            $("#m-fam-ava").addClass("hidden");
        }
    }
    });
    $(window).resize(function() {
    if ($(window).width() < 800) {
        $("#m-fam-ava").addClass("hidden");
        $(".container").addClass("hidden");
    }
});


