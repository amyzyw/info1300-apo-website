// Executive
$("#eb-yasmin").click(function() {
    if ($(window).width()>800) {
        $("#m-eb-yasmin").removeClass("hidden");
        $(".container").removeClass("hidden");
        $("#cancel-eb-yasmin").click(function() {
            $("#m-eb-yasmin").addClass("hidden");
            $(".container").addClass("hidden");
        });
    } else {
        if (!$("#m-eb-yasmin").hasClass("hidden")) {
            $("#m-eb-yasmin").addClass("hidden");
        }
    }
});
$(window).resize(function() {
    if ($(window).width() < 800) {
        $("#m-eb-yasmin").addClass("hidden");
        $(".container").addClass("hidden");
    }
  });


